S = 'Hello world!'

 # Does the string S start with 'Hello'?
print(f'\n {S.startswith("Hello")}')

 # Does the string S end with 'world'?
print(f'\n {S.endswith("world")}')

S1 = S.replace('world', 'people')

 # How many occurrences of the word 'amazing' does string S1 have?
print(f'\n {S1.count("amazing")}') 
